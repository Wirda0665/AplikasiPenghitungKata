// Fungsi untuk menghitung jumlah kata
private int hitungKata(String teks) {
    if (teks.trim().isEmpty()) return 0;
    String[] kata = teks.trim().split("\\s+");
    return kata.length;
}

// Fungsi untuk menghitung jumlah karakter (tanpa spasi)
private int hitungKarakter(String teks) {
    return teks.replaceAll("\\s+", "").length();
}

// Fungsi untuk menghitung jumlah kalimat
private int hitungKalimat(String teks) {
    if (teks.trim().isEmpty()) return 0;
    String[] kalimat = teks.split("[.!?]+");
    return kalimat.length;
}

// Fungsi menampilkan hasil
private void tampilkanHasil(String teks) {
    txtKata.setText(String.valueOf(hitungKata(teks)));
    txtKarakter.setText(String.valueOf(hitungKarakter(teks)));
    txtKalimat.setText(String.valueOf(hitungKalimat(teks)));
    txtParagraf.setText(String.valueOf(hitungParagraf(teks)));
}

// Hitung paragraf (dipisah oleh baris kosong)
private int hitungParagraf(String teks) {
    if (teks.trim().isEmpty()) return 0;
    String[] paragraf = teks.trim().split("\\n+"); // split berdasarkan newline
    return paragraf.length;
}

// Fungsi pencarian kata
private int cariKata(String teks, String kataDicari) {
    if (teks.trim().isEmpty() || kataDicari.trim().isEmpty()) return 0;
    String[] kata = teks.toLowerCase().split("\\s+");
    int count = 0;
    for (String k : kata) {
        if (k.replaceAll("[^a-zA-Z0-9]", "").equals(kataDicari.toLowerCase())) {
            count++;
        }
    }
    return count;
}



